def sockMerchant(n, ar):
    # Write your code here
    number_of_pairs = 0
    pairs = set()
    for sock in ar:
        if sock in pairs:
            number_of_pairs += 1
            pairs.remove(sock)
        else:
            pairs.add(sock)

    return number_of_pairs
  
